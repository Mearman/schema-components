/**
 * Generates `packages/docs/src/generated/api-urls.ts` from the TypeDoc
 * project model so Storybook DocsPages can render deep links to the
 * TypeDoc API reference for each symbol declared in
 * `parameters.apiSymbols` on a story meta.
 *
 * Output shape:
 *
 *     export const apiUrls: Readonly<Record<string, readonly ApiPage[]>> = {
 *         SchemaComponent: [
 *             { name: "SchemaComponent",
 *               modulePath: "react/SchemaComponent",
 *               kind: "Function",
 *               url: "https://mearman.github.io/schema-components/functions/react_SchemaComponent.SchemaComponent.html" },
 *         ],
 *         // ...
 *     };
 *
 * - Reads `typedoc-static/project.json` (produced by `pnpm typedoc`).
 * - Walks every module's top-level documented exports.
 * - Builds one entry per (name, modulePath) and groups by symbol name.
 * - Re-exports (TypeDoc kind 4194304) are skipped — the canonical
 *   definition is always also exported under its real kind from another
 *   module, and the README inventory follows the same convention.
 *
 * The bidirectional cross-link picture:
 *   - TypeDoc-land → Storybook: README inventory's Stories column.
 *   - Storybook-land → TypeDoc: the DocsPage "API reference" badge,
 *     powered by this generated map.
 */

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const projectJsonPath = resolve(here, "../typedoc-static/project.json");
const outputPath = resolve(
    here,
    "../../docs/src/generated/api-urls.ts",
);
const hostedBase = "https://mearman.github.io/schema-components";

const KIND = {
    Variable: 32,
    Function: 64,
    Class: 128,
    Interface: 256,
    TypeAlias: 2097152,
    Reference: 4194304,
    Enum: 8,
};

const KIND_LABEL = {
    [KIND.Variable]: "Variable",
    [KIND.Function]: "Function",
    [KIND.Class]: "Class",
    [KIND.Interface]: "Interface",
    [KIND.TypeAlias]: "Type",
    [KIND.Enum]: "Enum",
};

const KIND_URL_SEGMENT = {
    [KIND.Variable]: "variables",
    [KIND.Function]: "functions",
    [KIND.Class]: "classes",
    [KIND.Interface]: "interfaces",
    [KIND.TypeAlias]: "types",
    [KIND.Enum]: "enums",
};

function urlFor(kind, modulePath, name) {
    const segment = KIND_URL_SEGMENT[kind];
    if (!segment) return null;
    const moduleSlug = modulePath.replace(/\//g, "_");
    return `${hostedBase}/${segment}/${moduleSlug}.${name}.html`;
}

function isInternalNamespace(name) {
    return name === "<internal>";
}

function collectPages(project) {
    const bySymbol = new Map();
    for (const moduleRef of project.children ?? []) {
        const modulePath = moduleRef.name;
        for (const child of moduleRef.children ?? []) {
            if (isInternalNamespace(child.name)) continue;
            if (child.flags?.isPrivate) continue;
            const label = KIND_LABEL[child.kind];
            if (!label) continue;
            const url = urlFor(child.kind, modulePath, child.name);
            if (!url) continue;
            if (!bySymbol.has(child.name)) bySymbol.set(child.name, []);
            bySymbol.get(child.name).push({
                name: child.name,
                modulePath,
                kind: label,
                url,
            });
        }
    }
    return bySymbol;
}

function renderModule(bySymbol) {
    const lines = [];
    lines.push("/**");
    lines.push(" * Generated by packages/core/scripts/build-api-urls.mjs.");
    lines.push(" * Do not edit by hand — re-run `pnpm api-urls`.");
    lines.push(" *");
    lines.push(
        " * Maps each documented top-level symbol name to one or more",
    );
    lines.push(
        " * pages on the hosted TypeDoc site. Consumed by the Storybook",
    );
    lines.push(
        " * DocsPage decorator that renders an \"API reference\" badge",
    );
    lines.push(" * per symbol declared in `parameters.apiSymbols`.");
    lines.push(" */");
    lines.push("");
    lines.push("export interface ApiPage {");
    lines.push("    readonly name: string;");
    lines.push("    readonly modulePath: string;");
    lines.push(
        "    readonly kind:",
        '        | "Variable"',
        '        | "Function"',
        '        | "Class"',
        '        | "Interface"',
        '        | "Type"',
        '        | "Enum";',
    );
    lines.push("    readonly url: string;");
    lines.push("}");
    lines.push("");
    lines.push(
        "export const apiUrls: Readonly<Record<string, readonly ApiPage[]>> = {",
    );
    const sortedNames = [...bySymbol.keys()].sort((a, b) =>
        a.localeCompare(b),
    );
    for (const name of sortedNames) {
        const pages = bySymbol
            .get(name)
            .slice()
            .sort((a, b) => a.modulePath.localeCompare(b.modulePath));
        const key = /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(name);
        lines.push(`    ${key}: [`);
        for (const page of pages) {
            lines.push(
                `        { name: ${JSON.stringify(page.name)}, ` +
                    `modulePath: ${JSON.stringify(page.modulePath)}, ` +
                    `kind: ${JSON.stringify(page.kind)}, ` +
                    `url: ${JSON.stringify(page.url)} },`,
            );
        }
        lines.push("    ],");
    }
    lines.push("};");
    lines.push("");
    return lines.join("\n");
}

const project = JSON.parse(readFileSync(projectJsonPath, "utf8"));
const bySymbol = collectPages(project);
const source = renderModule(bySymbol);
mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, source, "utf8");
console.log(
    `[build-api-urls] wrote ${bySymbol.size} symbol entries to ${outputPath}`,
);
